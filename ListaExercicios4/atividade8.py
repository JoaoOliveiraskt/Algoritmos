num1 = int(input("Digite o primeiro número inteiro: "))
num2 = int(input("Digite o segundo número inteiro: "))
num_real = float(input("Digite o número real: "))

resultado_a = (2 * num1) * (num2 / 2)
resultado_b = (3 * num1) + num_real
resultado_c = num_real ** 3

print("O produto do dobro do primeiro com a metade do segundo é:", resultado_a)
print("A soma do triplo do primeiro com o terceiro é:", resultado_b)
print("O terceiro elevado ao cubo é:", resultado_c)